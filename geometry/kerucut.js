let jariJari = 7;
let tinggi = 10;
let garisPelukis = 12;

let volume = (1 / 3) * Math.PI * jariJari * jariJari * tinggi;
let luasPermukaan = Math.PI * jariJari * (jariJari + garisPelukis);

console.log("Volume Kerucut:", volume.toFixed(2));
console.log("Luas Permukaan:", luasPermukaan.toFixed(2));